
--Fund
select empid, sum(ownshare) as ownshare, sum(VPF) as vpf, sum(bankshare) as bankshare from (
select empid as empid,PFprin as ownshare,VPFprin as VPF,Bankprin as bankshare from PFopen p join pempmast m on p.eid=m.eid where year=2020 and m.eid in (select eid from poldslip where Pmonth='202012')  union all
select empid as empid ,pf as ownshare,p.VPF as VPF,p.PFcont as bankshare from poldslip p join pempmast m on p.eid=m.eid where Pmonth>=202004 and Pmonth<=202012 
and m.eid in (select eid from poldslip where Pmonth='202012')) as a group by empid order by empid

--Interest
select empid as empid,PFint as ownshare_int,VPFint as vpf_int,Bankint as bank_int from PFopen p join pempmast m on p.eid=m.eid where year=2020 
and m.eid in (select eid from poldslip where Pmonth='202012') order by empid
-------------------

--Non Repayable loan availed upto previous year
select m.empid,pfreturn, VPFreturn,Bankreturn from PFopen p join pempmast m on p.eid=m.eid where  year=2020 and m.eid in (select eid from poldslip where Pmonth='202012')
order by empid
--Non Repayable loan availed Present year
select m.empid, sum(Fownshare) as ownshare,sum(Fvpfshare) as vpfshare, sum(Fbankshare) as bankshare   from non_refAdvances p right join pempmast m on p.eid=m.eid 
and Pfmonth>=202004 and Pfmonth<=202012 where  m.eid in (select eid from poldslip where Pmonth='202012')
 group by empid
order by empid
-------------------

--Repayable PF Loan 1 Amount
select m.empid,Amount-CumLoanRepaid+IntRepaid as RepayablePFLoan1Amount from PoldLoan po right join pempmast m on m.Eid=po.Eid and  PMonth =202012  and LoanId='PFHT1' and pmonth=202012 
where m.eid in (select eid from poldslip where Pmonth='202012') order by empid;
--Repayable PF Loan 2 Amount
select m.empid,Amount-CumLoanRepaid+IntRepaid as RepayablePFLoan2Amount from PoldLoan po right join pempmast m on m.Eid=po.Eid and PMonth =202012  
and LoanId='PFHT2' and pmonth=202012 where m.eid in (select eid from poldslip where Pmonth='202012') order by empid;


